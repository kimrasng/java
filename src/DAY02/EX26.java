package DAY02;

public class EX26 {
    public static void main(String[] args) {
        // 비트 연산자
        int a = 7;
        int b = 4;

        int c = a & b;
        int d = a | b;
        int e = a ^ b;

        System.out.println("c = " + c);
        System.out.println("d = " + d);
        System.out.println("d = " + e);
    }
}
