package academy.DAY02;

public class EX21 {
    public static void main(String[] args) {
        // 관계 연산자
        int data1 = 2; double data2 = 2.1;

        System.out.println("data1 <= data2: " + (data1 <= data2));
        System.out.println("data1 >= data2: " + (data1 >= data2));
        System.out.println("data1 == data2: " + (data1 == data2));
        System.out.println("data1 != data2: " + (data1 != data2));
    }
}
