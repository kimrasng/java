package academy.DAY02;

public class EX19 {
    public static void main(String[] args) {
        // 산술 연산자
        int data1, data2;
        data1 = data2 = 5;

        System.out.println(data1 + " + " + data2 + " = " + (data1 + data2));
        System.out.println(data1 + " - " + data2 + " = " + (data1 - data2));
        System.out.println(data1 + " * " + data2 + " = " + (data1 * data2));
        System.out.println(data1 + " / " + data2 + " = " + (data1 / data2));
        System.out.println(data1 + " % " + data2 + " = " + (data1 % data2));

    }
}
