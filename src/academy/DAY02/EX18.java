package academy.DAY02;

import java.util.Scanner;
public class EX18 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        String st;
        int num;
        float f;

        st = scanner.nextLine();
        System.out.println(st);

        num = scanner.nextInt();
        System.out.println(num);

        f = scanner.nextFloat();
        System.out.println(f);

        scanner.close();
    }
}
