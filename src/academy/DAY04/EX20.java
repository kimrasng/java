package academy.DAY04;

public class EX20 {
    public static void main(String[] args){
        int[] arrayInt10 = {10, 20, 30, 40, 50};

        // for
        for (int i = 0; i < arrayInt10.length; i++){
            System.out.println(arrayInt10.length);
        }

        // for each
        for (int num:arrayInt10) {
            System.out.println(num);
        }
    }
}
